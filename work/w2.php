                <p class="description">Should I Shave or Should I Grow (SISOSIG) is a social media web application made for facial hair lovers. With it, users can share their facial hair (or lack thereof) and crowd source their answer to the ever-pressing question of 'Should I Shave Or Should I Grow?'
                <br><br>
                A user can create an account through our Sign Up Page or by signing up with their Facebook information. From there, the user can upload any photos of himself, which will end up on the Stubble Feed. The Stubble Feed is our version of a social feed where users can anonymously post pictures of themselves for others to vote on. Fellow users can help the user decide whether their facial hair is worthy of a full beard, or if it should profile page, which only he can see, to see how his posts were voted. 
                <br><br>
                SISOG is a group project created for a Web Application Development course at University of Rochester. To create this project, I worked with a group of 4 other developers. We utilized the MEAN stack - MongoDB, Express, Angular and Node.js. When creating the front end of our site, we decided to use Pug and Angular for our view engine. Angular and Express were used in conjunction to make posts. The URL contains the pipeline requests. To store all information about posts and people, we used one database with multiple collections.
