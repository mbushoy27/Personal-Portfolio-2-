            <div class="modalContainer">
                <p class="description">FDB Mobler is a Danish furniture company established in 1942. Danish design is renound for it's minimalist, though functionalist, design. FDB Mobler chairs perfectly exemplify Danish design, as these chairs have been a staple for decades in Danish homes. After closing its factory in 1980, FDB Mobler relocated a number of old classics in 2013; at that moment, an old classic was rediscovered. Today, FDB Mobler stores exist throughout Denmark and exhibit qualities of close family values together with creating a democratic community. To many Danish people, FDB Mobler means tradition and timelessness.
                <br><br>
                During my semester abroad in Copenhagen Denmark, we were tasked to recreate the brand identity for FDB Mobler. FDB Mobler represents the old and new. It's young parents shopping for FDB Mobler chairs today, reminiscing on the ones they had in their family kitchen years ago. It's a chair representing every day life for every generation. </p>

                <p class="mini-title"> Process </p>
                <img src="images/process1.png" style="width:600px;height:auto" class="gif"> <br>
                <p class="description">My process began by looking at the chairs and seeing how I could take inspiration from their forms. I noticed that there was a lot of linearity in the chairs themselves. The most notable FDB Mobler chairs were ones that included thin linear panels. 

                <br><br>Additionally, I found inspirations in the letters "F", "D" and "B", finding that these letters also stem from lines. When writing each of these letters, you always start by first making a line. From here, I looked at how the linear shapes of these letters could fit themselves into the linear panels of FDB Mobler chairs.</p>
                <img src="images/process2.png" style="width:600px;height:auto" class="gif"> <br>
                <p class="description">I took a closer look at my ideation process, and found that putting aspects of a physical chair into the logo might be too on the nose. I maintained my idea of a linear theme, though manifested it into a different plan. I thought about using the lines to convey an idea of "building and breaking", as you would when setting up furniture. I played around with incorporating that theme into the letters "F", "D" and "B". </p>
                <img src="images/process3.png" style="width:600px;height:auto" class="gif"> <br>
                <img src="images/process4.png" style="width:600px;height:auto" class="gif"> <br>
                <p class="description">Finally, I digitally mocked up my sketches. I looked to shirts, business cards and bags as a way to further visualize the brand as i continued to develop it.  </p>
